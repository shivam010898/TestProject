package com.lti.overridingEx;

public class Dog extends Animal {
	
	public void eat()
	{
		System.out.println("Eat method from dog class");
	}
	public void sleep()
	{
		System.out.println("sleep method from dog class");
	}
	public void bark()
	{
		System.out.println("bark method from dog class");
	}


}
